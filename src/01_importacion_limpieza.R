# ============================================================
# CARGA DE LIBRER√çAS NECESARIAS
# ============================================================
library(tidyverse)
library(ggplot2)
library(sf)
library(rnaturalearth)
library(rnaturalearthdata)
library(stringr)
library(modelr)
library(ggh4x)

# ============================================================
# IMPORTACION DE DATASETS
# ============================================================
df_train <- read_csv("data/train_oil.csv")
df_test <- read_csv("data/oil_test.csv")
df <- full_join(df_train, df_test)

# ============================================================
# LIMPIEZA Y TRANSFORMACI√ìN INICIAL DE VARIABLES
# ============================================================

# üìò 1. Renombrado de columnas
# Se traducen los nombres originales del dataset a espa√±ol y se estandariza el formato.
df <- df %>% rename(
  campo                 = `Field name`,
  unidad_reservorio     = `Reservoir unit`,
  pais                  = `Country`,
  region                = `Region`,
  cuenca                = `Basin name`,
  regimen_tectonico     = `Tectonic regime`,
  latitud               = `Latitude`,
  longitud              = `Longitude`,
  operadora             = `Operator company`,
  plataforma            = `Onshore/Offshore`,
  tipo_hidrocarburo     = `Hydrocarbon type`,
  estado_reservorio     = `Reservoir status`,
  estructura_geologica  = `Structural setting`,
  profundidad           = `Depth`,
  periodo_geologico     = `Reservoir period`,
  litologia             = `Lithology`,
  espesor_bruto         = `Thickness (gross average ft)`,
  espesor_neto          = `Thickness (net pay average ft)`,
  porosidad             = `Porosity`,
  permeabilidad         = `Permeability`
)

# 2. Relleno de valores faltantes (NA)
# - Completamos regi√≥n, longitud y latitud usando variables relacionadas como campo, pa√≠s, cuenca y unidad.
# - Esto mejora la integridad de los datos y permite usar el mapa posteriormente sin p√©rdida de registros.

df <- df %>%
  group_by(campo) %>%
  fill(pais, region, longitud, latitud, .direction = "downup") %>%
  ungroup() %>%
  group_by(pais) %>%
  fill(region, .direction = "downup") %>%
  ungroup() %>%
  group_by(unidad_reservorio) %>%
  fill(region, .direction = "downup") %>%
  ungroup() %>%
  group_by(cuenca) %>%
  fill(region, .direction = "downup") %>%
  ungroup()

# 3. Eliminaci√≥n de variables no relevantes
# Se descartan campos que no aportan al objetivo del an√°lisis (nombre del campo, operadora, etc.)
df <- df %>%
  mutate(
    campo = NULL,
    unidad_reservorio = NULL,
    pais = NULL,
    cuenca = NULL,
    operadora = NULL,
    plataforma = NULL,
    estado_reservorio = NULL,
    estructura_geologica = NULL
  )

# 4. Traducci√≥n de categor√≠as de "regi√≥n"
# Mejora la presentaci√≥n en los gr√°ficos y facilita la lectura.
df <- df %>%
  mutate(region = case_when(
    region == "FORMER SOVIET UNION" ~ "EX UNI√ìN SOVI√âTICA",
    region == "LATIN AMERICA"       ~ "AM√âRICA LATINA",
    region == "MIDDLE EAST"         ~ "MEDIO ORIENTE",
    region == "EUROPE"              ~ "EUROPA",
    region == "NORTH AMERICA"       ~ "AM√âRICA DEL NORTE",
    region == "AFRICA"              ~ "√ÅFRICA",
    region == "FAR EAST"            ~ "LEJANO ORIENTE",
    TRUE ~ region
  ))

# 5. Reagrupamiento del r√©gimen tect√≥nico
# Clasificamos los reg√≠menes en cuatro grandes grupos tect√≥nicos:
# - CONVERGENTE, DIVERGENTE, TRANSFORMANTE y MIXTO
# - La clasificaci√≥n se basa en la presencia de t√©rminos clave en la descripci√≥n original.

df <- df %>%
  filter(str_detect(regimen_tectonico, "COMPRESSION|INVERSION|TRANSPRESSION|STRIKE-SLIP|TRANSTENSION|EXTENSION")) %>%
  mutate(regimen_tectonico = case_when(
    str_detect(regimen_tectonico, "COMPRESSION|INVERSION") & str_detect(regimen_tectonico, "EXTENSION") ~ "MIXTO",
    str_detect(regimen_tectonico, "COMPRESSION|INVERSION") & str_detect(regimen_tectonico, "TRANSPRESSION|STRIKE-SLIP|TRANSTENSION") ~ "MIXTO",
    str_detect(regimen_tectonico, "EXTENSION") & str_detect(regimen_tectonico, "TRANSPRESSION|STRIKE-SLIP|TRANSTENSION") ~ "MIXTO",
    str_detect(regimen_tectonico, "COMPRESSION|INVERSION") ~ "CONVERGENTE",
    str_detect(regimen_tectonico, "TRANSPRESSION|STRIKE-SLIP|TRANSTENSION") ~ "TRANSFORMANTE",
    str_detect(regimen_tectonico, "EXTENSION") ~ "DIVERGENTE",
    TRUE ~ regimen_tectonico
  ))

# 6. Simplificaci√≥n posterior de TRANSFORMANTE ‚Üí MIXTO
# Justificado por bajo n√∫mero de casos y coherencia con los criterios tect√≥nicos.
df <- df %>%
  mutate(regimen_tectonico = ifelse(regimen_tectonico == "TRANSFORMANTE", "MIXTO", regimen_tectonico))


# ============================================================
# Reagrupamiento de per√≠odos geol√≥gicos
# ============================================================

# Objetivo:
# Agrupar los 28 per√≠odos geol√≥gicos en categor√≠as m√°s robustas 
# desde el punto de vista estad√≠stico y geocronol√≥gico.

# Observaciones iniciales:
# - S√≥lo 9 de los 28 per√≠odos tienen m√°s de 6 observaciones.
# - 4 per√≠odos superan las 40 observaciones.
# - Esta dispersi√≥n genera desequilibrios en el an√°lisis.

# Estrategia considerada:
# - Primero evaluamos agrupar por eras (Prec√°mbrica, Paleozoica, Mesozoica, Cenozoica),
#   pero descartamos esa opci√≥n por dos motivos:

#   1) Se pierden patrones relevantes al unir m√∫ltiples per√≠odos en una sola era.
#   2) La Prec√°mbrica sigue siendo escasa en datos incluso agrupada como era.

# Limpieza adicional:
# - Se elimin√≥ "Archean" por tener solo 1 observaci√≥n y gran distancia temporal.
# - Se descartaron "Mesozoic" y "Paleozoic" por estar mal clasificadas (son eras, no per√≠odos).

# Resultado:
# - Se definieron 8 grupos cronol√≥gicos contiguos.
# - Estos reflejan tanto coherencia temporal como significancia estad√≠stica en el dataset.

df <- df %>%
  mutate(periodo_geologico=str_trim(periodo_geologico))%>%
  mutate(periodo_geologico = case_when(
    periodo_geologico %in% c("PROTEROZOIC", 
                         "PROTEROZOIC-CAMBRIAN",
                         "CAMBRIAN", 
                         "CAMBRIAN-ORDOVICIAN",
                         "ORDOVICIAN", 
                         "SILURIAN", 
                         "DEVONIAN-PERMIAN",
                         "DEVONIAN")~"PROTEROZOICO-DEV√ìNICO",
    periodo_geologico %in% c("CARBONIFEROUS",
                                 "DEVONIAN-CARBONIFEROUS",
                                 "CAMBRIAN-ORDOVICIAN/CARBONIFEROUS",
                         "CARBONIFEROUS-PERMIAN",
                         
                                 "CARBONIFEROUS-CRETACEOUS"
                                 )~"CARBON√çFERO",
    periodo_geologico %in% c("PERMIAN", 
              "PERMIAN-TRIASSIC"
              
              )~"P√âRMICO",
    periodo_geologico%in% c("TRIASSIC","TRIASSIC-JURASSIC")~"TRI√ÅSICO",
    periodo_geologico %in% c("JURASSIC","JURASSIC-CRETACEOUS")~"JUR√ÅSICO",
    periodo_geologico %in% c("CRETACEOUS","CRETACEOUS-PALEOGENE","PALEOZOIC-CRETACEOUS")~"CRET√ÅCICO",
    periodo_geologico%in% c("PALEOGENE","PALEOGENE-NEOGENE")~"PALE√ìGENO",
    periodo_geologico=="NEOGENE"~"NE√ìGENO",
    TRUE~"OTROS"))%>%
  filter(periodo_geologico!="OTROS")

# ============================================================
# LIMPIEZA Y TRANSFORMACI√ìN DE VARIABLES GEOL√ìGICAS
# ============================================================

# 1. Reagrupamiento de la litolog√≠a
# Agrupamos m√∫ltiples tipos espec√≠ficos de sandstone y limestone bajo esas categor√≠as generales.

df <- df %>%
  mutate(
    litologia = case_when(
      str_detect(litologia, "SANDSTONE") ~ "SANDSTONE",
      str_detect(litologia, "LIMESTONE") ~ "LIMESTONE",
      TRUE ~ litologia
    )
  )

# 2. Traducci√≥n de litolog√≠as al espa√±ol
df <- df %>%
  mutate(litologia = case_when(
    litologia == "SANDSTONE"     ~ "ARENISCA",
    litologia == "LIMESTONE"     ~ "CALIZA",
    litologia == "DOLOMITE"      ~ "DOLOMITA",
    litologia == "CONGLOMERATE"  ~ "CONGLOMERADO",
    litologia == "SILTSTONE"     ~ "LUTITA LIMOSA",
    litologia == "CHALK"         ~ "CRETA",
    litologia == "SHALE"         ~ "LUTITA",
    litologia == "VOLCANICS"     ~ "ROCA VOLC√ÅNICA",
    litologia == "DIATOMITE"     ~ "DIATOMITA",
    litologia == "CHERT"         ~ "S√çLEX",
    litologia == "BASEMENT"      ~ "BASAMENTO",
    TRUE ~ litologia
  ))

# 3. Reducci√≥n de categor√≠as poco representadas
# Por criterio estad√≠stico, unificamos las litolog√≠as menos frecuentes bajo "OTRAS"
df <- df %>%
  mutate(litologia = case_when(
    litologia %in% c("ARENISCA", "CALIZA", "DOLOMITA") ~ litologia,
    TRUE ~ "OTRAS"
  ))

# 4. Filtrado de tipos de hidrocarburos
# Se eliminan hidrocarburos no convencionales con muy baja representaci√≥n (BITUMEN, CO2, etc.)
df <- df %>%
  filter(tipo_hidrocarburo %in% c("OIL", "GAS", "GAS-CONDENSATE"))

# 5. Validaci√≥n y filtrado de espesor neto
# - Se eliminan filas con espesor neto mayor al bruto (sin sentido f√≠sico).
# - Se eliminan registros con espesor neto igual a 0 (probable error de carga).
df <- df %>%
  filter(espesor_neto <= espesor_bruto, espesor_neto > 0)

# 6. Eliminaci√≥n de outlier extremo en porosidad
# Se elimina el valor 55 por estar fuera del rango f√≠sico esperado (0‚Äì30%) y fuera de IQR*1.5
df <- df %>%
  filter(porosidad < 55)

# 7. Conversi√≥n de unidades a sistema m√©trico
# Se convierten profundidad y espesor bruto de pies a kil√≥metros
df <- df %>%
  mutate(
    profundidad    = profundidad * 0.0003048,
    espesor_bruto  = espesor_bruto * 0.0003048
  )

# 8. Filtrado de valores extremos en permeabilidad
# Se conserva el 95% de los registros (distribuci√≥n recortada) para evitar sesgos de cola
df <- df %>%
  filter(permeabilidad < quantile(permeabilidad, 0.95))

# 9. Conversi√≥n de variables categ√≥ricas a factores
# Se definen los niveles ordenados para las eras geol√≥gicas
df <- df %>%
  mutate(
    regimen_tectonico = factor(regimen_tectonico),
    tipo_hidrocarburo = factor(tipo_hidrocarburo),
    litologia         = factor(litologia),
    periodo_geologico = factor(periodo_geologico,
                               levels = c(
                                 "PROTEROZOICO-DEV√ìNICO",
                                 "CARBON√çFERO",
                                 "P√âRMICO",
                                 "TRI√ÅSICO",
                                 "JUR√ÅSICO",
                                 "CRET√ÅCICO",
                                 "PALE√ìGENO",
                                 "NE√ìGENO")
    )
  )

# ============================================================
# TRANSFORMACIONES PARA MEJORAR EL AJUSTE DEL MODELO
# ============================================================

# 1. Log-transformaci√≥n del espesor bruto
# - La variable espesor bruto presenta una distribuci√≥n sesgada a la derecha.
# - Este comportamiento es esperable seg√∫n el dominio: los espesores var√≠an ampliamente por contexto estructural.
# - Aplicamos el logaritmo natural para estabilizar la varianza y reducir la influencia de outliers.
# - Esto mejora el ajuste en un modelo lineal al captar relaciones no lineales.

df <- df %>%
  mutate(espesor_bruto_log = log(espesor_bruto))

# 2. Log-transformaci√≥n de la permeabilidad
# - Durante el an√°lisis exploratorio, observamos que la relaci√≥n entre permeabilidad y porosidad no era lineal.
# - Por eso aplicamos log-transformaci√≥n para permitir un mejor ajuste del modelo.

df <- df %>%
  mutate(permeabilidad_log = log(permeabilidad))
